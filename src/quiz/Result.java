 public ArrayList<User3> userList(){
        ArrayList<User3> userList = new ArrayList<User3>();
        try{
            Connection connection = (Connection) DriverManager.getConnection("jdbc:mysql://localhost:3308/quiz?",
                        "root", "a7n8s4h0");
            String query="SELECT * FROM SCORE ORDER BY SCORE DESC";
            Statement st = connection.createStatement();
            ResultSet rs = st.executeQuery(query); 
            User3 user;
            while(rs.next())
            {
                user = new  User3(
                        rs.getString("USER"),
                        rs.getInt("SCORE"));
                userList.add(user);
            }
         }
        catch (SQLException sqlException) 
                {
            sqlException.printStackTrace();
                }
     return userList;
    }
  public void show_user()
    {
        ArrayList<User3> list = userList();
        DefaultTableModel model = (DefaultTableModel)scoreTable.getModel();
        Object[] row = new Object[2];
        for(int i=0;i<list.size();i++)
        {
            row[0] = list.get(i).getname();
            row[1] = list.get(i).getscore();
            model.addRow(row);  
        }
        
    }
